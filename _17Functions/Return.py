
def add(num1, num2):
    result = num1 + num2
    return result

def subract(num1, num2):
    result = num1 - num2
    return result

def multiply(num1, num2):
    result = num1 * num2
    return result

def divide(num1, num2):
    result = num1 / num2
    return result

print(add(12,13))
print(subract(12,13))
print(multiply(12,13))
print(divide(12,13))