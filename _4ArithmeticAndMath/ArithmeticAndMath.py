import math

friends = 5

# augmented assignment operator
friends += 1 # friends = friends + 1 addition
friends -= 2 # friends = friends - 2 subtraction
friends *= 3 # friends = friends * 3 multiplication
friends /= 4 # friends = friend / 4 division
friends **= 2 # friends = friends ** 2 exponentiation
friends %= 3 # modulus

print(friends)

x = 3.14
y = -4
z = 5

# built-in math operators
print(round(x)) # round off a value
print(abs(y)) # absolute value
print(pow(z, 6)) # power
print(max(x, y, z)) # maximum value of a set of numbers
print(min(x, y, z)) # minimum value of a set of numbers

# import math
print(math.pi) # pi value
print(math.e) # euler value
print(math.sqrt(9)) # squareroot function
print(math.ceil(9.4)) # always round a number up
print(math.floor(17.87)) # always round a number down